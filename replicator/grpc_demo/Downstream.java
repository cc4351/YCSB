/**
 * Downstream server to handle forwarding requests.
 */
public class Downstream {
  private static final Logger logger = Logger.getLogger(Downstream.class.getName());

  private final int port;
  private final Server server;

  public Downstream(int port) throws IOException {
    // TODO: would be where we setup rocksdb
    this.port = port;
    ServerBuilder serverBuilder = ServerBuilder.forPort(port);
    server = serverBuilder.addService(new DownstreamService()).build();
  }

  /** Start serving requests. */
  public void start() throws IOException {
    server.start();
    logger.info("Server started, listening on " + port);
    Runtime.getRuntime().addShutdownHook(new Thread() {
      @Override
      public void run() {
        // Use stderr here since the logger may have been reset by its JVM shutdown hook.
        System.err.println("*** shutting down gRPC server since JVM is shutting down");
        try {
          Downstream.this.stop();
        } catch (InterruptedException e) {
          e.printStackTrace(System.err);
        }
        System.err.println("*** server shut down");
      }
    });
  }

  /** Stop serving requests and shutdown resources. */
  public void stop() throws InterruptedException {
    if (server != null) {
      server.shutdown().awaitTermination(30, TimeUnit.SECONDS);
    }
  }

  /**
   * Await termination on the main thread since the grpc library uses daemon threads.
   */
  private void blockUntilShutdown() throws InterruptedException {
    if (server != null) {
      server.awaitTermination();
    }
  }

  /**
   * Main method.  This comment makes the linter happy.
   */
  public static void main(String[] args) throws Exception {
    Downstream server = new Downstream(8980);
    server.start();
    server.blockUntilShutdown();
  }

  /**
   * Our implementation of RouteGuide service.
   *
   * <p>See route_guide.proto for details of the methods.
   */
  private static class DownstreamService extends N2NGrpc.N2NImplBase {
    // private final Collection<Feature> features;
    // private final ConcurrentMap<Point, List<RouteNote>> routeNotes =
    //     new ConcurrentHashMap<Point, List<RouteNote>>();

    // DownstreamService() {
    //   this.features = features;
    // }

    /**
     * Gets the {@link Feature} at the requested {@link Point}. If no feature at that location
     * exists, an unnamed feature is returned at the provided location.
     *
     * @param request the requested location for the feature.
     * @param responseObserver the observer that will receive the feature at the requested point.
     */
    @Override
    public void forward(Request request, StreamObserver<Dull> responseObserver) {
      System.out.println("request: " + request);
      responseObserver.onNext(Dull.newBuilder().build());
      responseObserver.onCompleted();
    }
  }
}

